public class Two_D_Array {
    public static void main(String[] args) {
        int[][] arr = {
                {10, 20, 30, 40},
                {15, 25, 35, 45},
                {28, 29, 37, 49},
                {33, 34, 38, 50}
        };

        System.out.println("Number of rows: " + arr.length);
        System.out.println("Number of columns: " + arr[0].length);
    }
}
